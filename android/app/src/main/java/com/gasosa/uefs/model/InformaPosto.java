package com.gasosa.uefs.model;

public class InformaPosto {

    String borracharia;
    String oleo;
    String loja;
    String foto;
    String horario;
    String nome;
    String profile;

    public InformaPosto() {

    }

    public String getProfile() {
        return profile;
    }

    public void setProfile(String profile) {
        this.profile = profile;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getBorracharia() {
        return borracharia;
    }

    public void setBorracharia(String borracharia) {
        this.borracharia = borracharia;
    }

    public String getOleo() {
        return oleo;
    }

    public void setOleo(String oleo) {
        this.oleo = oleo;
    }

    public String getLoja() {
        return loja;
    }

    public void setLoja(String loja) {
        this.loja = loja;
    }

    public String getFoto() {
        return foto;
    }

    public void setFoto(String foto) {
        this.foto = foto;
    }

    public String getHorario() {
        return horario;
    }

    public void setHorario(String horario) {
        this.horario = horario;
    }
}
